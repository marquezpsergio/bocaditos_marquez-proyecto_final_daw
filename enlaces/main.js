// FUNCIONES COLORES HOVER ACCESO Y CARRITO //
function color_acceso() {
    $("#acceso_icono").css("color", "yellow");
    $("#acceso_texto").css("color", "yellow");
}

function color_acceso_normal() {
    document.getElementById("acceso_icono").style.color = "white";
    document.getElementById("acceso_texto").style.color = "white";
}

